                            自从孙悟空当上了“齐天大圣”之后就好久没有回家乡——花果山了！有一天，他想花果山的“孩儿”们了，变向玉帝请了一个探亲假去探望他的“孩儿”们了！<br>
                            他翻了一个跟头，筋斗云便迅速的赶来。孙悟空驾上那筋斗云朝花果山飞去。<br>
                            一到花果山孙悟空就说：“咦！这那里是俺老孙的故乡呢？是不是来错了？”他正在准备乘云离去时，忽然，一面“齐天大圣”的旗子映入孙悟空的眼帘。接着他看到了焕然一新的水帘洞。只见水帘洞口挂着一串串五光十色的彩灯把水帘洞装点的分外美丽。山上果树成林，三下农田如画、电线密布，还有一幢幢高低错落的房屋。<br>
                            孙悟空看着家乡的巨变，心里好不快活。忽然，天暗了下来，他抬头一看，一个长有巨大翅膀的庞然大物正从头顶飞过，他从耳朵里掏出一根绣花针，说：“长！”顿时，绣花针变成了金箍棒。他对着那个庞然大物说：“何方妖怪？吃俺老孙一棒！”说完他立刻打下去，才挥了一半，那庞然大物就迅速向前飞行了好远，气流把孙悟空吹到了十万八千里的地方。这是何方妖怪？怎么法力如此高强？孙悟空恼羞成怒，他急中生智说：“土地、土地！”土地来了，他把事情的经过告诉了土地，土地哈哈大笑说：“大圣，那不是什么妖怪，而是农用飞机，专门给花果山的一草一木浇水、喷农药，时不时佛祖还会夸奖它呢！”孙悟空听了恍然大悟。接着土地公公带孙悟空游览了花果山，介绍了拖拉机、收割机、脱粒机……现代机械，孙悟空看得目瞪口呆，连连称奇。听了土地的一番简介他不由自主的说：“人间真发达！俺老孙要好好研究去了！”<br>
